package com.meryemalay;

public class Sofa {

    private String manufacturer;
    private String fabric;

    public Sofa(String manufacturer, String fabric) {
        this.manufacturer = manufacturer;
        this.fabric = fabric;
    }

    public String getManufacturer() {
        return manufacturer;
    }

    public String getFabric() {
        return fabric;
    }
}
